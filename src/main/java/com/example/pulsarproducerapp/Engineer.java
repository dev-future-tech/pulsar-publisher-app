package com.example.pulsarproducerapp;

public class Engineer {

    private long engineerId;

    private String engineeringRole;

    private String engineerRegion;

    public long getEngineerId() {
        return engineerId;
    }

    public void setEngineerId(long engineerId) {
        this.engineerId = engineerId;
    }

    public String getEngineeringRole() {
        return engineeringRole;
    }

    public void setEngineeringRole(String engineeringRole) {
        this.engineeringRole = engineeringRole;
    }

    public String getEngineerRegion() {
        return engineerRegion;
    }

    public void setEngineerRegion(String engineerRegion) {
        this.engineerRegion = engineerRegion;
    }
}
